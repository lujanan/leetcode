package algorithm_0

import "testing"

func Test_climbStairs1(t *testing.T) {
	type args struct {
		n int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{"t1", args{1}, 1},
		{"t2", args{2}, 2},
		{"t3", args{3}, 3},
		{"t4", args{4}, 5},
		{"t5", args{5}, 8},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := climbStairs(tt.args.n); got != tt.want {
				t.Errorf("climbStairs1() = %v, want %v", got, tt.want)
			}
		})
	}
}
