package algorithm_0

func searchMatrix(matrix [][]int, target int) bool {
	var w = len(matrix[0])
	var l, r = 0, len(matrix)*w - 1

	for l <= r {
		var m = l + (r-l)>>1
		if target == matrix[m/w][m%w] {
			return true
		}

		if target < matrix[m/w][m%w] {
			r = m - 1
		} else {
			l = m + 1
		}
	}
	return false
}
