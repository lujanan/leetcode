package algorithm_0

func longestCommonPrefix(strs []string) string {
	var res []byte
	if len(strs) > 0 {
		str := strs[0]
		for i := range str {
			for j := 1; j < len(strs); j++ {
				if i >= len(strs[j]) || strs[j][i] != str[i] {
					return string(res)
				}
			}
			res = append(res, str[i])
		}
	}

	return string(res)
}
