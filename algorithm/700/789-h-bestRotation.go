func bestRotation1(nums []int) int {
	var res, mi, ti int
	var n = len(nums)
	for k := 0; k < n; k++ {
		ti = 0
		for i := 0; i < n; i++ {
			if nums[(k+i)%n] <= i {
				ti++
			}
		}
		if mi < ti {
			mi = ti
			res = k
		}
	}

	return res
}
