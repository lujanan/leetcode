package algorithm_700

func bestRotation(nums []int) int {
	var res, rmi, lmi int
	var n = len(nums)
	var kl = make([]int, len(nums)+1)
	for i := 0; i < n; i++ {
		rmi = (i - nums[i] + n) % n
		// lmi = rmi + nums[i] - n + 1
		lmi = (i - n + 1 + n) % n

		// i - nums[i] + nums[i] -n + 1
		// nums[i] - i + nums[i] -n + 1

		kl[rmi+1]--
		if lmi >= 0 {
			kl[lmi]++
		} else {
			kl[0]++
			kl[n+lmi]++
		}
	}

	for i := 1; i < n; i++ {
		kl[i] += kl[i-1]
		if kl[res] < kl[i] {
			res = i
		}
	}
	return res
}

