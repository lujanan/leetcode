func bestRotation2(nums []int) int {
	var res, rmi, lmi, nli int
	var n = len(nums)
	var kl = make([]int, len(nums))
	for i := 0; i < n; i++ {
		rmi = i - nums[i]
		if rmi < 0 {
			rmi += n
		}

		nli = n - nums[i]
		lmi = rmi - nli + 1
		if lmi >= 0 {
			kl[lmi]++
		} else {
			kl[0]++
			kl[n+lmi]++
		}

		if rmi+1 < n {
			kl[rmi+1]--
		}
	}

	for i := 1; i < n; i++ {
		kl[i] += kl[i-1]
		if kl[res] < kl[i] {
			res = i
		}
	}
	return res
}

