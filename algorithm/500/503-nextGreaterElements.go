func nextGreaterElements(nums []int) (res []int) {
	var stack []int
	res = make([]int, len(nums))
	for i := 2*len(nums) - 1; i >= 0; i-- {
		j := len(stack) - 1
		for j >= 0 {
			if nums[i%len(nums)] < stack[j] {
				break
			}
			j--
		}
		stack = stack[:j+1]
		if len(stack) > 0 {
			res[i%len(nums)] = stack[len(stack)-1]
		} else {
			res[i%len(nums)] = -1
		}
		stack = append(stack, nums[i%len(nums)])
	}
	return
}
